    <h2 class="title">Available Shortcodes</h2>

    <hr>

    <blockquote>
        <p>For Filter Pages use <code>[flex_idx_filter id="" mode="" type="" limit="" method=""]</code></p>
        <p>For Filter Pages in grid view with external collection link <code>[flex_idx_filter id="MZEZY2IW" mode="grid" max="6" target_id="35" target_label=""]</code></p>
        <p>For Agent Filter Pages use <code>[idx_agent_filter id="" mode="default" type="agent|office" searchbar="yes|no"]</code></p>
        <p>For Building Group Pages use <code>[idx_buildind_group building_id="" mode="default" class=""]</code></p>
        <p>For Autocomplete <code>[flex_autocomplete]</code></p>
        <p>For Quick Search Form <code>[flex_idx_quick_search]</code></p>
        <p>For Account Links <code>[ib_account_links]</code></p>
        <p>For Website Contact Form <code>[flex_idx_contact_form]</code></p>
    </blockquote>

